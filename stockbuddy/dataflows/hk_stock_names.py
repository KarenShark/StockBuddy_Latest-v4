def get_hk_stock_chinese_name(ticker: str) -> str:
    """
    获取港股的中文名称
    
    Args:
        ticker: 股票代码，如 "0700", "0700.HK", "700" 等
    
    Returns:
        中文公司名称，如果未找到则返回 None
    """
    # 清理ticker格式
    clean_ticker = ticker.upper().replace('.HK', '').replace('.HKG', '')
    
    # 补全为4位数字
    if clean_ticker.isdigit():
        clean_ticker = clean_ticker.zfill(4)
    
    return HK_STOCK_NAMES.get(clean_ticker)


def get_all_hk_stock_codes():
    """
    获取所有已映射的港股代码列表
    """
    return list(HK_STOCK_NAMES.keys())


def update_hk_stock_name(ticker: str, chinese_name: str):
    """
    添加或更新港股中文名称映射
    
    Args:
        ticker: 股票代码
        chinese_name: 中文公司名称
    """
    clean_ticker = ticker.upper().replace('.HK', '').replace('.HKG', '')
    if clean_ticker.isdigit():
        clean_ticker = clean_ticker.zfill(4)
    HK_STOCK_NAMES[clean_ticker] = chinese_name
